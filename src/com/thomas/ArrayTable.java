package com.thomas;


public class ArrayTable {

    public static void main(String[] args) {

        int myArray[] = {2, 23, 54, 19, 888};

        System.out.println("Index\tValue");;

        for (int i = 0; i < myArray.length; i++) {

            System.out.println(i + "\t\t" + myArray[i]);

        }

    }

}
